package com.mkpits.math;

public class MathDemo {

	public static void main(String[] args) {
		
		
		System.out.println(Math.PI);
		System.out.println(Math.E);
		System.out.println(Math.abs(2.796565744454494645));
		System.out.println(Math.acos(2));
		System.out.println(Math.addExact(54354353,5445446 ));
		System.out.println(Math.cbrt(69));
		System.out.println(Math.ceil(5435687.3553545345));
		System.out.println(Math.copySign(652.89, -0.545));// dusre number ki sign copy karta hai
		System.out.println(Math.cos(30));
		System.out.println(Math.cosh(545));
		System.out.println(Math.decrementExact(55544));//exaact decrement by one
		System.out.println(Math.max(878, 4555));//returns max number
		System.out.println(Math.toIntExact(445445154l));
		System.out.println(Math.fma(2, 8, 2));//(a*b+c)
		System.out.println(Math.floorDiv(74, 8));//(divide karke quotient dega)
		System.out.println(Math.round(534344.566));//roundoff
		System.out.println(Math.IEEEremainder(56, 5));
		
		
		
	}

}

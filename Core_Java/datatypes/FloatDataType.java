package com.mkpits.datatypes;

public class FloatDataType {

	public static void main(String[] args) {
		
		// to calculate the range of datatype use -2^n-1  to  (2^n-1)-1.
		// Stores fractional numbers. Sufficient for storing 6 to 7 decimal digits
//		Literal - Floating - float , double.
//		default value - 0.0
//		 float memory required 4 bytes
		// put F/f after assigning the value
		
		float a = 998872.99F;
		float minPercentage = 0.01F;
		
		System.out.println("Minimum percentage obtained are" + minPercentage + " and Maximum percentage obbtained are" + a);

	}

}
